my_fruits = {'grape', 'cherry', 'banana'}
bestfr_fruits = {'cherry', 'strawberry', 'blueberry'}

union_result = my_fruits.union(bestfr_fruits)

intersection_result = my_fruits.intersection(bestfr_fruits)

difference_result = my_fruits.difference(bestfr_fruits)

print('Intersection:', intersection_result)
print('Union:', union_result)
print('Difference:', difference_result)