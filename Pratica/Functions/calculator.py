def add(a, b):
  answer = a + b
  return answer

def subtract(a, b):
  answer = a - b
  return answer

def multiply(a, b):
  answer = a * b
  return answer

def divide(a, b):
  answer = a / b
  return answer

def exp(a, b):
  answer = a ** b
  return answer

print(add(5, 10))
print(subtract(5, 10))
print(multiply(5, 10))
print(divide(5, 10))
print(exp(5, 10))